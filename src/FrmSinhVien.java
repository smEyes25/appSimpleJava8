    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String ten = txtTen.getText();
        double diem = Double.parseDouble(txtDiem.getText());
        boolean phai = true ? rbNam.isSelected() : false;
        String lop = cbLop.getSelectedItem().toString();
        String keyLop = lop.substring(0, lop.indexOf(" ")).trim();
        
        SinhVien _sv = null;
        
        if(!checkMaSV(maSV + 1)) {
            _sv = new SinhVien(maSV + 1, ten, phai, diem, keyLop);
        } else {
            for(int i = 1; i <= dsSV.size(); i++) {
                if(!checkMaSV(i)) {
                    _sv = new SinhVien(i, ten, phai, diem, keyLop);
                    break;
                }
            }
        }
        
        dsSV.put(_sv.getMa(), _sv);
        dsLop.get(_sv.getMaLop()).themSV(_sv);
        
        
        for(SinhVien curSV : dsLop.get(dsSV.get(_sv.getMa()).getMaLop()).getDsSV().values()) {
            System.out.println(curSV.getMa() + " " + curSV.getTen() + " " + curSV.isPhai() + " " + curSV.getDiem() + " " + curSV.getMaLop());
        }
        
        capNhatDSSV();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tbDssv.getSelectedRow();
        int key = (int) tModel.getValueAt(row, 0);
        
        String tenLop = cbLop.getSelectedItem().toString();
        String maLop = tenLop.substring(0, tenLop.indexOf(" "));
     
        if(row >= 0) {
            dsLop.get(maLop).xoaSV(dsSV.get(key));
            dsSV.remove(key);
            
            txtTen.setText("");
            txtDiem.setText("");
            rbNam.setSelected(true);
            cbLop.setSelectedIndex(0);
            
            capNhatDSSV();
        } 
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tbDssvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDssvMouseClicked
        int row = tbDssv.getSelectedRow();
        int key = (int) tModel.getValueAt(row, 0);
        
        if(row >= 0) {
            sv = dsSV.get(key);
            txtTen.setText(sv.getTen());
            txtDiem.setText(String.valueOf(sv.getDiem()));
            if(sv.isPhai()) rbNam.setSelected(true);
            else rbNu.setSelected(true);
            
            Lop curLop = dsLop.get(sv.getMaLop());
            
            cbLop.setSelectedItem(curLop.getMa() + " - " + curLop.getTen());
        }
    }//GEN-LAST:event_tbDssvMouseClicked

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tbDssv.getSelectedRow();
        int key = (int) tModel.getValueAt(row, 0);
        
        sv = dsSV.get(key);
        sv.setTen(txtTen.getText());
        sv.setDiem(Double.parseDouble(txtDiem.getText()));
        sv.setPhai(rbNam.isSelected() ? true : false);
        
        String tenLop = cbLop.getSelectedItem().toString();
        
        if(tenLop.equals(sv.getMaLop())) {
            dsLop.get(dsSV.get(key).getMaLop()).capNhatSV(sv.getMa(), sv);
        } else {
            dsLop.get(dsSV.get(key).getMaLop()).xoaSV(dsSV.get(key));
            dsSV.get(key).setMaLop(tenLop.substring(0, tenLop.indexOf(" ")));
            dsLop.get(dsSV.get(key).getMaLop()).themSV(dsSV.get(key));
        }
        
        capNhatDSSV();
    }//GEN-LAST:event_btnSuaActionPerformed

