    public void capNhatDSSV() {
        tModel.setRowCount(0);
        
        for(SinhVien sv : dsSV.values()) {
            tModel.addRow(new Object[]{sv.getMa(), sv.getTen(), sv.isPhai() == true ? "Nam" : "Nu", sv.getDiem(), sv.getMaLop()});
        }
        
        maSV = dsSV.size();
    }
    
    public static void capNhatDSLop() {
        cbModel.removeAllElements();
        
        dsLop.values().stream().map((lop) -> lop.getMa() + " - " + lop.getTen()).forEachOrdered(cbModel::addElement);
    }
    
    public boolean checkMaSV(int masv) {
        return dsSV.get(masv) != null ? true : false;
    }

